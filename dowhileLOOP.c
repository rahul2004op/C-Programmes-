# include<stdio.h>
int main(){
    // print thr numbers in ascendind order.
    int i=1;
    do {
        printf("%d\n",i);
        i++;
    }while(i<=5);

    //print the numbers in descending order.
    int a=10;
    do {
        printf("%d\n",a);
        a--;
    }while(a>=1);
    return 0;
}